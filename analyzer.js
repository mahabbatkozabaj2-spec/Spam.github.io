function analyzeUrlLocal(url) {
  const scores = {phishing: 0, spam: 0, fraud: 0, manipulation: 0};
  const flags = [], details = [];

  let fullUrl = url;
  if (!fullUrl.startsWith('http://') && !fullUrl.startsWith('https://')) fullUrl = 'http://' + fullUrl;

  let parsed;
  try { parsed = new URL(fullUrl); } catch {
    return _buildResult(100, {phishing:100,spam:0,fraud:0,manipulation:0}, ['Невозможно разобрать URL'], [], 'url', url);
  }

  const domain = parsed.hostname.toLowerCase().replace(/^www\./, '').split(':')[0];
  const full = fullUrl.toLowerCase();

  // 1. HTTP
  if (parsed.protocol === 'http:') {
    scores.phishing += 18;
    flags.push('Нет HTTPS — данные передаются открыто');
  }

  // 2. Безопасные домены
  if ([...SAFE_DOMAINS].some(s => domain === s || domain.endsWith('.' + s))) {
    details.push(`Домен "${domain}" — в базе надёжных ресурсов`);
    return _buildResult(Math.max(0, _calcTotal(scores)), scores, flags, details, 'url', url);
  }

  // 3. Энтропия
  const ent = _domainEntropy(domain);
  if (ent > 3.8) {
    scores.phishing += 22;
    flags.push(`Случайное имя домена (энтропия=${ent.toFixed(1)})`);
  } else if (ent > 3.2) {
    scores.spam += 10;
    flags.push(`Подозрительное имя домена (энтропия=${ent.toFixed(1)})`);
  }

  // 4. Подозрительный TLD
  for (const tld of SUSPICIOUS_TLD) {
    if (domain.endsWith(tld)) {
      scores.phishing += 25;
      flags.push(`Подозрительная зона: ${tld}`);
      break;
    }
  }

  // 5. Typosquatting
  const tsq = _checkTyposquatting(domain);
  if (tsq.found) {
    scores.phishing += 50;
    flags.push(`Typosquatting: имитация "${tsq.brand}" (разница=${tsq.dist} симв.)`);
    details.push(`ОПАСНО: домен "${domain}" почти совпадает с ${tsq.brand}!`);
  }

  // 6. Leet-speak
  if (!tsq.found) {
    for (const [pat, brand] of PHISHING_PATTERNS_DOMAIN) {
      if (pat.test(domain)) {
        scores.phishing += 40;
        flags.push(`Имитация бренда ${brand} через замену символов`);
        break;
      }
    }
  }

  // 7. IP вместо домена
  if (/^\d{1,3}(\.\d{1,3}){3}$/.test(domain)) {
    scores.phishing += 35;
    flags.push('IP-адрес вместо домена — признак фишинга');
  }

  // 8. Много поддоменов
  const parts = domain.split('.');
  if (parts.length > 4) {
    scores.phishing += 20;
    flags.push(`Слишком много поддоменов (${parts.length} уровней)`);
  }

  // 9. @ в URL
  if (full.includes('@')) {
    scores.phishing += 28;
    flags.push('Символ "@" в URL — трюк для скрытия адреса');
  }

  // 10. Сокращатели
  for (const s of URL_SHORTENERS) {
    if (domain.includes(s)) {
      scores.spam += 15;
      flags.push(`Сокращатель ссылок (${s}) — адрес скрыт`);
      break;
    }
  }

  // 11. Подозрительные слова
  const sus = ['login','verify','account','secure','update','confirm','banking',
    'password','free','win','prize','gift','reward','activate','signin',
    'recover','unlock','suspended','blocked','limited','support'];
  const found = sus.filter(w => full.includes(w));
  if (found.length) {
    scores.phishing += Math.min(found.length * 9, 28);
    flags.push(`Подозрительные слова в URL: ${found.slice(0, 4).join(', ')}`);
  }

  // 12. Спам-слова в пути
  const spamPath = ['free','win','prize','earn','make-money','casino','bonus','lucky'];
  if (spamPath.some(w => full.includes(w))) {
    scores.spam += 18;
    flags.push('Спам-тематика в URL');
  }

  // 13. Закодирование
  const pct = (full.match(/%/g) || []).length;
  if (pct > 5) {
    scores.phishing += Math.min(pct * 2, 20);
    flags.push(`Чрезмерное URL-кодирование (${pct} символов "%")`);
  }

  // 14. Длинный URL
  if (url.length > 200) {
    scores.fraud += 10;
    flags.push(`Аномально длинный URL (${url.length} симв.)`);
  }

  // 15. Фишинговые слова в домене
  const scamWordsDomain = ['secure','bank','login','verify','update','account',
    'service','support','official','help','payment','pay'];
  if (scamWordsDomain.some(w => domain.includes(w))) {
    scores.phishing += 12;
    flags.push('Фишинговые слова в доменном имени');
  }

  return _buildResult(_calcTotal(scores), scores, flags, details, 'url', url);
}

