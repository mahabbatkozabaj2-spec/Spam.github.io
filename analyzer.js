function _kwHits(text, keywords) {
  return keywords.filter(k => text.includes(k.toLowerCase()));
}

function _levenshtein(s1, s2) {
  if (s1.length < s2.length) return _levenshtein(s2, s1);
  if (!s2.length) return s1.length;
  let prev = Array.from({length: s2.length + 1}, (_, i) => i);
  for (const c1 of s1) {
    const curr = [prev[0] + 1];
    for (let j = 0; j < s2.length; j++) {
      curr.push(Math.min(prev[j + 1] + 1, curr[j] + 1, prev[j] + (c1 !== s2[j] ? 1 : 0)));
    }
    prev = curr;
  }
  return prev[prev.length - 1];
}

function _domainEntropy(domain) {
  const name = domain.split('.')[0];
  if (name.length < 4) return 0;
  const freq = {};
  for (const ch of name) freq[ch] = (freq[ch] || 0) + 1;
  const n = name.length;
  return -Object.values(freq).reduce((sum, c) => sum + (c / n) * Math.log2(c / n), 0);
}

function _checkTyposquatting(domain) {
  const bare = domain.toLowerCase().replace(/^www\./, '').replace(/\.[a-z]{2,}$/, '').replace(/[-_]/g, '');
  let best = null, dist = 999;
  for (const brand of Object.keys(BRAND_DOMAINS)) {
    const d = _levenshtein(bare, brand);
    const thr = brand.length <= 5 ? 1 : brand.length <= 8 ? 2 : 3;
    if (d > 0 && d <= thr && d < dist) {
      const real = BRAND_DOMAINS[brand];
      if (domain !== real && !domain.endsWith('.' + real)) {
        dist = d; best = brand;
      }
    }
  }
  return { found: best !== null, brand: best, dist };
}

function _calcTotal(scores) {
  // Усиленные веса: manipulation теперь 0.18 (был 0.10)
  const w = {phishing: 0.36, fraud: 0.33, spam: 0.13, manipulation: 0.18};
  const raw = Object.entries(w).reduce((sum, [k, v]) => sum + (scores[k] || 0) * v, 0);
  const peak = Math.max(...Object.values(scores));
  // Пик умножается на 0.85 вместо 0.82 — более чувствительно
  return Math.min(Math.floor(Math.max(raw, peak * 0.85)), 100);
}

function _parseDomain(url) {
  try {
    if (!url.startsWith('http://') && !url.startsWith('https://')) url = 'http://' + url;
    const u = new URL(url);
    return u.hostname.toLowerCase().replace(/^www\./, '').split(':')[0];
  } catch {
    return url.toLowerCase();
  }
}

