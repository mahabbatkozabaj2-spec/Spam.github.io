def analyze_text(text: str, filename: str = '') -> dict:
    base   = analyze_message(text)
    scores = dict(base.get('category_scores', {'phishing':0,'spam':0,'fraud':0,'manipulation':0}))
    flags  = list(base['flags'])
    details= list(base['details'])
    tl     = text.lower()

    # URL's в документе
    all_urls = re.findall(r'https?://\S+|www\.\S+', text)
    bad_urls = []
    for u in all_urls[:20]:
        r = analyze_url(u.rstrip('.,;)"\''))
        if r['risk_score'] > 30:
            bad_urls.append((u[:70], r['risk_score']))
    if bad_urls:
        scores['phishing'] += min(len(bad_urls)*12, 40)
        flags.append(f'Подозрительных URL в документе: {len(bad_urls)} шт.')
        details.append('Топ угроз: '+' | '.join([f'{u}({s}%)' for u,s in bad_urls[:3]]))

    # Email-заголовки (.eml)
    if 'from:' in tl and ('subject:' in tl or 'to:' in tl):
        details.append('Обнаружены email-заголовки — анализ письма')
        from_m = re.search(r'from:\s*[^<]*<(.+?)>', text, re.I) or re.search(r'from:\s*(\S+@\S+)', text, re.I)
        if from_m:
            sender_domain = from_m.group(1).lower().split('@')[-1]
            for brand, real in BRAND_DOMAINS.items():
                if brand in sender_domain and sender_domain != real:
                    scores['phishing'] += 45
                    flags.append(f'Поддельный email — имитирует {brand.title()}')
                    details.append(f'Реальный отправитель "{sender_domain}" — НЕ официальный {brand}!')
                    break
        repl_m = re.search(r'reply-to:\s*(\S+@\S+)', text, re.I)
        if repl_m and from_m:
            if repl_m.group(1).split('@')[-1] != from_m.group(1).split('@')[-1]:
                scores['phishing'] += 22
                flags.append('Reply-To отличается от From — скрытие реального адреса')
        if re.search(r'(spf=fail|dkim=fail|dmarc=fail)', tl):
            scores['phishing'] += 28
            flags.append('Провал SPF/DKIM/DMARC — письмо не прошло проверку')

    # Опасные расширения
    danger_ext = ['.exe','.bat','.cmd','.vbs','.js','.jar','.ps1','.scr','.hta','.pif']
    found_ext = [e for e in danger_ext if e in tl]
    if found_ext:
        scores['fraud'] += 22
        flags.append(f'Упоминание опасных расширений: {", ".join(found_ext)}')

    total = _calc_total(scores)
    return _build_result(total, scores, flags, details, 'document', filename)


# ════════════════════════════════════ ПОСТРОЕНИЕ РЕЗУЛЬТАТА ═════

def _calc_dominant(scores: dict) -> str:
    labels = {'phishing':'Фишинг','fraud':'Мошенничество','spam':'Спам','manipulation':'Манипуляция'}
    if not scores or all(v==0 for v in scores.values()):
        return 'Угроз не выявлено'
    top = max(scores, key=scores.get)
    return labels.get(top, top)

def _build_result(score, scores, flags, details, atype, subject):
    score = int(min(round(score), 100))

    if score <= 12:
        lvl,verdict,desc,stype,icon = (
            'safe','Нет явных угроз',
            'Система не нашла опасных паттернов, но вы всё равно должны быть осторожны.',
            'Неизвестно','ℹ️')
    elif score <= 38:
        lvl,verdict,desc,stype,icon = (
            'low','Низкий риск — проверьте отправителя',
            'Найдены незначительные признаки. Соблюдайте осторожность.',
            'Вероятно безопасен, но требует проверки','WARN')
    elif score <= 62:
        lvl,verdict,desc,stype,icon = (
            'medium','Средний риск — ПОДОЗРИТЕЛЬНО',
            'Явные признаки спама или нечестного поведения. Не доверяйте.',
            'Вероятно СПАМЕР или недобросовестный продавец','ALERT')
    else:
        lvl,verdict,desc,stype,icon = (
            'high','ВЫСОКИЙ РИСК — МОШЕННИК!',
            'Многочисленные признаки мошенничества. НЕ переводите деньги и НЕ передавайте данные!',
            'МОШЕННИК / ФИШИНГОВАЯ АТАКА','DANGER')

    return {
        'risk_score':      score,
        'risk_level':      lvl,
        'verdict':         verdict,
        'description':     desc,
        'sender_type':     stype,
        'icon':            icon,
        'flags':           flags,
        'details':         details,
        'recommendations': _get_recommendations(lvl, flags),
        'category_scores': scores,
        'dominant_threat': _calc_dominant(scores),
        'analysis_type':   atype,
        'subject':         subject,
        'timestamp':       datetime.now().isoformat(),
    }

