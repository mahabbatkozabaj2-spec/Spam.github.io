def analyze_url(url: str) -> dict:
    scores = {'phishing':0,'spam':0,'fraud':0,'manipulation':0}
    flags, details = [], []

    if not url.startswith(('http://','https://')):
        url = 'http://' + url
    try:
        parsed = urllib.parse.urlparse(url)
    except Exception:
        return _build_result(100,{'phishing':100,'spam':0,'fraud':0,'manipulation':0},
                             ['Невозможно разобрать URL'],[],  'url', url)

    domain = parsed.netloc.lower().replace('www.','').split(':')[0]
    full   = url.lower()

    # 1. HTTP
    if parsed.scheme == 'http':
        scores['phishing'] += 18
        flags.append('Нет HTTPS — данные передаются открыто')

    # 2. Безопасные домены — ранний выход
    if any(domain == s or domain.endswith('.'+s) for s in SAFE_DOMAINS):
        details.append(f'Домен "{domain}" — в базе надёжных ресурсов')
        return _build_result(max(0,_calc_total(scores)), scores, flags, details, 'url', url)

    # 3. Энтропия (случайный домен)
    ent = _domain_entropy(domain)
    if ent > 3.8:
        scores['phishing'] += 22; flags.append(f'Случайное имя домена (энтропия={ent:.1f})')
    elif ent > 3.2:
        scores['spam'] += 10; flags.append(f'Подозрительное имя домена (энтропия={ent:.1f})')

    # 4. Подозрительный TLD
    for tld in SUSPICIOUS_TLD:
        if domain.endswith(tld):
            scores['phishing'] += 25
            flags.append(f'Подозрительная зона: {tld}'); break

    # 5. Typosquatting
    is_sq, sq_brand, sq_dist = _check_typosquatting(domain)
    if is_sq:
        scores['phishing'] += 50
        flags.append(f'Typosquatting: имитация "{sq_brand}" (разница={sq_dist} симв.)')
        details.append(f'ОПАСНО: домен "{domain}" почти совпадает с {sq_brand}!')

    # 6. Leet-speak
    if not is_sq:
        for pat, brand in PHISHING_PATTERNS_DOMAIN:
            if re.search(pat, domain):
                scores['phishing'] += 40
                flags.append(f'Имитация бренда {brand} через замену символов'); break

    # 7. IP вместо домена
    if re.match(r'^\d{1,3}(\.\d{1,3}){3}$', domain):
        scores['phishing'] += 35
        flags.append('IP-адрес вместо домена — признак фишинга')

    # 8. Много поддоменов
    parts = domain.split('.')
    if len(parts) > 4:
        scores['phishing'] += 20
        flags.append(f'Слишком много поддоменов ({len(parts)} уровней)')

    # 9. @ в URL
    if '@' in full:
        scores['phishing'] += 28
        flags.append('Символ "@" в URL — трюк для скрытия адреса')

    # 10. Сокращатели
    for s in URL_SHORTENERS:
        if s in domain:
            scores['spam'] += 15
            flags.append(f'Сокращатель ссылок ({s}) — адрес скрыт'); break

    # 11. Подозрительные слова
    sus = ['login','verify','account','secure','update','confirm','banking',
           'password','free','win','prize','gift','reward','activate','signin',
           'recover','unlock','suspended','blocked','limited','support']
    found = [w for w in sus if w in full]
    if found:
        scores['phishing'] += min(len(found)*9, 28)
        flags.append(f'Подозрительные слова в URL: {", ".join(found[:4])}')

    # 12. Спам-слова в пути
    spam_path = ['free','win','prize','earn','make-money','casino','bonus','lucky']
    if any(w in full for w in spam_path):
        scores['spam'] += 18; flags.append('Спам-тематика в URL')

    # 13. Избыточное закодирование
    pct = full.count('%')
    if pct > 5:
        scores['phishing'] += min(pct*2, 20)
        flags.append(f'Чрезмерное URL-кодирование ({pct} символов "%")')

    # 14. Очень длинный URL
    if len(url) > 200:
        scores['fraud'] += 10; flags.append(f'Аномально длинный URL ({len(url)} симв.)')

    # 15. Подозрительные слова SCAM в домене
    scam_words_domain = ['secure','bank','login','verify','update','account',
                         'service','support','official','help','payment','pay']
    if any(w in domain for w in scam_words_domain):
        scores['phishing'] += 12
        flags.append('Фишинговые слова в доменном имени')

    return _build_result(_calc_total(scores), scores, flags, details, 'url', url)


# ════════════════════════════════════ АНАЛИЗ СООБЩЕНИЯ ══════════

def analyze_message(message: str) -> dict:
    scores = {'phishing':0,'spam':0,'fraud':0,'manipulation':0}
    flags, details = [], []
    tl = message.lower()

    # 1. Спам-слова (RU + KZ)
    spam_hits = _kw_hits(tl, SPAM_KEYWORDS)
    spam_hits_kz = _kw_hits(tl, SPAM_KEYWORDS_KZ)
    all_spam_hits = spam_hits + spam_hits_kz
    if all_spam_hits:
        scores['spam'] += min(len(all_spam_hits)*8, 55)
        lang_note = ' (каз)' if spam_hits_kz and not spam_hits else (' (рус+каз)' if spam_hits_kz else '')
        flags.append(f'Спам-ключевые слова{lang_note}: {len(all_spam_hits)} шт.')
        details.append(f'Найдено: «{", ".join(all_spam_hits[:6])}»' + ('…' if len(all_spam_hits)>6 else ''))

    # 2. Мошеннические слова (RU + KZ)
    scam_hits = _kw_hits(tl, SCAM_KEYWORDS)
    scam_hits_kz = _kw_hits(tl, SCAM_KEYWORDS_KZ)
    all_scam_hits = scam_hits + scam_hits_kz
    if all_scam_hits:
        scores['fraud'] += min(len(all_scam_hits)*14, 65)
        lang_note = ' (каз)' if scam_hits_kz and not scam_hits else (' (рус+каз)' if scam_hits_kz else '')
        flags.append(f'Мошеннические слова{lang_note}: {len(all_scam_hits)} шт.')
        details.append(f'Опасные слова: «{", ".join(all_scam_hits[:5])}»' + ('…' if len(all_scam_hits)>5 else ''))

    # 3. Манипулятивные фразы
    manip_hits = []
    for pat, label in MANIPULATION_PATTERNS:
        if re.search(pat, tl):
            manip_hits.append(label)
    if manip_hits:
        scores['manipulation'] += min(len(manip_hits)*12, 48)
        flags.append(f'Манипулятивные приёмы: {len(manip_hits)} шт.')
        details.append('Приёмы: ' + '; '.join(manip_hits[:4]))

    # 4. Составные scam-паттерны
    for name, pats, min_hits, f_score, s_score, detail in SCAM_COMBO_PATTERNS:
        hits = sum(1 for p in pats if re.search(p, tl))
        if hits >= min_hits:
            scores['fraud'] += f_score
            scores['spam']  += s_score
            flags.append(f'[СКАМ-СХЕМА] {name}')
            details.append(f'ВНИМАНИЕ: {detail}')

    # 5. Запрос личных данных
    pers_hits = [p for p in PERSONAL_DATA_PATTERNS if re.search(p, tl)]
    if pers_hits:
        scores['fraud']    += 40
        scores['phishing'] += 20
        flags.append('Запрос конфиденциальных данных')
        details.append('НИКОГДА не сообщайте CVV, PIN, коды из SMS в переписке!')

    # 5.1 Номер карты в тексте
    card_rx = re.findall(r'\b\d{4}[\s\-]?\d{4}[\s\-]?\d{4}[\s\-]?\d{4}\b', message)
    if card_rx:
        scores['phishing'] += 35
        scores['fraud'] += 20
        flags.append('Номер банковской карты в сообщении — возможный фишинг!')
        details.append('НИКОГДА не передавайте номера карт через сообщения!')

    # 5.2 QR код / ссылка
    if re.search(r'qr.?код|сканируйте|ссылка\s+для\s+оплат|ссылка\s+на\s+оплат', tl):
        scores['phishing'] += 20
        flags.append('QR-код или ссылка для оплаты')
        details.append('Не сканируйте QR-коды от незнакомых!')

    # 6. Деньги (валюты)
    money_rx = re.findall(
        r'(\d[\d\s,\.]{1,15})\s*(тыс|млн|миллион|тысяч|долларов|рублей|евро|'
        r'тенге|сом|гривен|лир|юан|фунт|usd|eur|rub|kzt|kgs|uah|₽|\$|€|₸|₴)',
        tl)
    if money_rx:
        scores['spam']  += 12
        scores['fraud'] +=  6
        amounts = [' '.join(m).strip() for m in money_rx[:4]]
        flags.append(f'Денежные суммы: {", ".join(amounts)}')

    # 7. Крупные числа (6+ цифр) → огромный депозит
    big_nums = re.findall(r'\b\d[\d\s]{5,}\d\b', tl)
    if big_nums:
        scores['fraud'] += 18
        flags.append('Фигурируют очень крупные суммы — признак скама')
        details.append('Мошенники используют огромные суммы для запугивания или обмана.')

    # 8. Заглавные буквы
    alpha = [c for c in message if c.isalpha()]
    if len(alpha) > 20:
        upper_r = sum(1 for c in alpha if c.isupper()) / len(alpha)
        if upper_r > 0.45:
            scores['manipulation'] += 14
            flags.append(f'Агрессивные ЗАГЛАВНЫЕ БУКВЫ ({int(upper_r*100)}%)')

    # 9. Восклицательные знаки
    excl = message.count('!')
    if excl > 3:
        scores['manipulation'] += min(excl*3, 15)
        flags.append(f'Избыточная эмоциональность ({excl} "!")')

    # 9.1 Избыточные эмодзи
    emoji_count = len(re.findall(r'[\U0001f300-\U0001f5ff\U0001f900-\U0001f9ff\U0001f600-\U0001f64f\U0001f680-\U0001f6ff\u2600-\u26ff\u2700-\u27bf]', message))
    if emoji_count > 8:
        scores['spam'] += min(emoji_count, 20)
        flags.append(f'Избыточное количество emoji ({emoji_count}шт.) — признак спама')

    # 10. Ссылки в тексте
    urls = re.findall(r'https?://\S+|www\.\S+', message)
    if urls:
        bad_urls = []
        for u in urls[:5]:
            r = analyze_url(u.rstrip('.,;)"\''))
            if r['risk_score'] > 30:
                bad_urls.append((u[:60], r['risk_score']))
        if bad_urls:
            scores['phishing'] += min(sum(s for _,s in bad_urls)//len(bad_urls), 55)
            flags.append(f'Подозрительные ссылки: {len(bad_urls)} шт.')
            details.append(f'Опасная ссылка: {bad_urls[0][0]}')
        else:
            scores['spam'] += min(len(urls)*5, 18)
            if urls: flags.append(f'Ссылки в сообщении: {len(urls)} шт.')

    # 11. Телефоны
    phones = re.findall(r'(\+7|8|\+[1-9]\d{0,2})[\s\-\(]?\d{3}[\s\-]?\d{3}[\s\-]?\d{2,4}[\s\-]?\d{0,4}', message)
    if phones:
        scores['spam'] += 8
        flags.append('Телефонный номер — возможный контакт мошенника')

    # 12. Нигерийские письма
    nig = [
        r'(миллион|million|млн).{0,30}(долларов|dollars|usd|тенге|рублей|сом)',
        r'(принц|prince|король|king|шейх).{0,40}(деньги|money|transfer)',
        r'(наследство|inheritance|завещание).{0,30}(получ|claim|transfer)',
    ]
    for sig in nig:
        if re.search(sig, tl):
            scores['fraud'] += 38
            flags.append('Признаки "нигерийского письма"')
            details.append('Классика мошенничества: обещают долю от суммы за "помощь".')
            break

    # 13. Смешение кириллицы и латиницы (авто-перевод)
    mixed = re.findall(r'[а-яёА-ЯЁ]{3,}[a-zA-Z]{2,}|[a-zA-Z]{3,}[а-яёА-ЯЁ]{2,}', message)
    if len(mixed) > 2:
        scores['spam'] += 10
        flags.append('Смешение кириллицы/латиницы — признак авто-перевода')

    # 14. Упоминание платёжных систем (RU + KZ)
    pay_sys = ['western union','moneygram','qiwi','юmoney','яндекс деньги',
               'perfect money','payeer','webmoney','bitcoin wallet','крипто кошелёк',
               # Казахские / KZ
               'kaspi арқылы жіберіңіз','kaspi transfer','қасби арқылы',
               'qiwi арқылы жіберіңіз','крипто жіберіңіз','биткоин жіберіңіз',
               'крипто кошелег','crypto wallet']
    pay_hits = [p for p in pay_sys if p in tl]
    if pay_hits:
        scores['fraud'] += min(len(pay_hits)*15, 40)
        flags.append(f'Упоминание анонимных платёжных систем: {", ".join(pay_hits[:3])}')
        details.append('Мошенники предпочитают анонимные системы — деньги не вернуть! / Алаяқтар анонимді жүйелерді пайдаланады!')

    # 15. Слова-триггеры «официальности» (RU + KZ — имитация госорганов/банков)
    official_fake = [
        # Русские
        r'(служба безопасности|отдел безопасности|антифрод)',
        r'(налоговая|следственный комитет|прокуратура|интерпол)',
        r'(официальный представитель|уполномоченный сотрудник)',
        r'(по решению суда|во избежание ареста|уголовное дело)',
        # Казахские
        r'(банктің қауіпсіздік|қауіпсіздік бөлімі|антифрод бөлімі)',
        r'(салық комитеті|прокуратура|полиция хабарлайды|ұлттық банк)',
        r'(ресми өкіл|уәкілетті қызметкер|мемлекеттік орган)',
        r'(сот шешімімен|тұтқындалуды болдырмау үшін|қылмыстық іс)',
        r'(kaspi банк қауіпсіздік|halyk bank хабар|fortebank хабар)',
    ]
    off_hits = [p for p in official_fake if re.search(p, tl)]
    if off_hits:
        scores['fraud']    += min(len(off_hits)*20, 55)
        scores['phishing'] += 15
        flags.append('Имитация госорганов или службы безопасности / Мемлекеттік органды еліктеу')
        details.append('Настоящие банки и госорганы НИКОГДА не звонят/пишут с просьбой о переводе! / Нақты банктер ешқашан аударым сұрамайды!')

    total = _calc_total(scores)
    subj  = (message[:100]+'…') if len(message)>100 else message
    return _build_result(total, scores, flags, details, 'message', subj)


