def _kw_hits(text: str, keywords: list) -> list:
    return [k for k in keywords if k.lower() in text]

def _levenshtein(s1: str, s2: str) -> int:
    if len(s1) < len(s2): return _levenshtein(s2, s1)
    if not s2: return len(s1)
    prev = list(range(len(s2)+1))
    for c1 in s1:
        curr = [prev[0]+1]
        for j, c2 in enumerate(s2):
            curr.append(min(prev[j+1]+1, curr[j]+1, prev[j]+(c1!=c2)))
        prev = curr
    return prev[-1]

def _domain_entropy(domain: str) -> float:
    name = domain.split('.')[0]
    if len(name) < 4: return 0.0
    freq = {}
    for ch in name: freq[ch] = freq.get(ch,0)+1
    n = len(name)
    return -sum((c/n)*math.log2(c/n) for c in freq.values())

def _check_typosquatting(domain: str):
    bare = re.sub(r'\.[a-z]{2,}$','', domain.lower().replace('www.',''))
    bare = re.sub(r'[-_]','', bare)
    best, dist = None, 999
    for brand in BRAND_DOMAINS:
        d = _levenshtein(bare, brand)
        thr = 1 if len(brand)<=5 else (2 if len(brand)<=8 else 3)
        if 0 < d <= thr < dist:
            real = BRAND_DOMAINS[brand]
            if domain != real and not domain.endswith('.'+real):
                dist, best = d, brand
    return best is not None, best, dist

def _calc_total(scores: dict) -> int:
    w = {'phishing':0.38,'fraud':0.35,'spam':0.17,'manipulation':0.10}
    raw = sum(scores.get(k,0)*v for k,v in w.items())
    peak = max(scores.values()) if scores else 0
    return min(int(max(raw, peak*0.82)), 100)


